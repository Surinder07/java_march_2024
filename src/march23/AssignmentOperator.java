package march23;

public class AssignmentOperator {
    public static void main(String[] args){

        System.out.println("Before Swapping");
        int x = 10;
        int y = 20;

        System.out.println("Value of x : "+x);
        System.out.println("Value of y : "+y);


        System.out.println("After swapping");
        // x = 20, y = 10

        int temp = x; //  temp = 10
        x = y;  // x = 20
        y = temp; // y = 10

        System.out.println("Value of x : "+x);
        System.out.println("Value of y : "+y);




    }
}


// int --- 0
// String - null
// Double -- 0.0
// boolean - false

